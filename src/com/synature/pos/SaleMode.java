package com.synature.pos;

public class SaleMode{
	private int SMODEID;
	private String SMODENAM;
	private int POSPRE;
	private String PRETXT;
	private String PREQUE;
	public int getSMODEID() {
		return SMODEID;
	}
	public void setSMODEID(int sMODEID) {
		SMODEID = sMODEID;
	}
	public String getSMODENAM() {
		return SMODENAM;
	}
	public void setSMODENAM(String sMODENAM) {
		SMODENAM = sMODENAM;
	}
	public int getPOSPRE() {
		return POSPRE;
	}
	public void setPOSPRE(int pOSPRE) {
		POSPRE = pOSPRE;
	}
	public String getPRETXT() {
		return PRETXT;
	}
	public void setPRETXT(String pRETXT) {
		PRETXT = pRETXT;
	}
	public String getPREQUE() {
		return PREQUE;
	}
	public void setPREQUE(String pREQUE) {
		PREQUE = pREQUE;
	}
}
